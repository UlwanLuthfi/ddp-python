# Operator Aritmatika
x = 10
y = 3

print("x =", x, "dan", "y =", y)

# Operasi Penjumlahan
hasil = x + y
print("Hasil x + y =", hasil)

# Operasi Pengurangan
hasil = x - y
print("Hasil x - y =", hasil)

# Operasi Perkalian
hasil = x * y
print("Hasil x * y =", hasil)

# Operasi Pembagian
hasil = x / y
print("Hasil x / y =", hasil)

# Operasi Eksponen
hasil = x**y
print("Hasil x ** y =", hasil)

# Operasi Modulus
hasil = x % y
print("Hasil x % y =", hasil)

# Operasi Floor Division
hasil = x // y
print("Hasil x // y =", hasil)

# Prioritas
hasil = x + y * x / (y - x) ** x
print("Hasil x + y * x / (y - x) ** x =", hasil)

# Operator Pembanding
x = 3
y = 5

print("x =", x, "dan", "y =", y)

# Equal (=)
hasil = x == y
print("x == y =", hasil)

# Not Equal (!=)
hasil = x != y
print("x != y =", hasil)

# Lebih Dari (>)
hasil = x > y
print("x > y =", hasil)

# Kurang Dari (<)
hasil = x < y
print("x < y =", hasil)

# Lebih Dari Sama Dengan (>=)
hasil = x >= y
print("x >= y =", hasil)

# Kurang Dari Sama Dengan (<=)
hasil = x <= y
print("x <= y =", hasil)
